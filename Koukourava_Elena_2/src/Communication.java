
public class Communication {
	
	private String thl1,thl2;
	private int mera,mhnas,xronos;
	
	public Communication (String thl1, String thl2, int mera, int mhnas, int xronos) {
		
		this.thl1 = thl1;
		this.thl2 = thl2;
		this.mera = mera;
		this.mhnas = mhnas;
		this.xronos = xronos;
		
	}

	public String getThl1() {
		
		return thl1;
	}
	public String getThl2() {
	
		return thl2;
	}

	public int getMera() {
		return mera;
	}

	public int getMhnas() {
		return mhnas;
	}

	public int getXronos() {
		return xronos;
	}
	
} 
